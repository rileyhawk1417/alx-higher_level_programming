#!/usr/bin/python3

def add(a, b):
    """Add function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """Subtract function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """Multiplication function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """Divison function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a / b
    """
    return (a / b)
